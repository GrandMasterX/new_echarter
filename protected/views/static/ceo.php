                    на курорт в любой город или другой пункт назначения, но при этом самолет ожидает пассажиров и на обратном рейсе возвращает их в пункт отправления.
                    Сплит-чартер – это перелет с пересадкой с регулярного на чартерный рейс. Наиболее часто распространены полеты в тропические страны и на малопосещаемые
                    курортные острова, до которых летают только самолеты малой вместимости.
                    Поличартерный перелет – перелет, в котором есть 2 и более чартерных рейса. Используется для дальних перелетов, а также в слабо посещаемые страны Азии и
                    Африки.
                </p>
