                <h3>Разновидности чартерных рейсов</h3>
                <p>Чартерами принято называть нерегулярные авиарейсы, о которых чаще всего
                    договариваются между собой туристические операторы и авиакомпании. Такое
                    соглашение выгодно всем участникам, поскольку у перевозчика не простаивают
                    самолеты и он получает дополнительную прибыль, туроператоры могут отправить
                    пассажиров прямыми чартерами на любые курорты или в другие места, интересные
                    туристам, а пассажиры имеют возможность приобрести дешевые авиабилеты. Чартер -
                    это возможность быстро, дешево, без пересадок оказаться на морском курорте или в
                    другом популярном для туристов месте.</p>
                <p>Чартерные рейсы выполняются не малоизвестными перевозчиками на сомнительных
                    самолетах, как опасаются некоторые пассажиры, а теми же авиакомпаниями, которые
                    отправляют свои авиалайнеры в регулярные полеты – но чартеры вписываются в
                    расписание между традиционными рейсами, из-за этого они нередко задерживаются с
                    вылетом.</p>
                <p>Бывалые путешественники знают, что бывают разные виды чартеров:</p>
                <ul>
                    <li>шаттлы – сезонные челночные авиарейсы, которые отвозят одних курортников
                        отдыхать, а назад забирают тех, кто возвращается с отдыха;</li>
                    <li>чартер с простоем – разовые рейсы, когда самолет отвозит группу
                        пассажиров и ожидает их в аэропорту назначения для обратного перелета;</li>
                    <li>сплит-чартер – перелет, предусматривающий пересадку с регулярного рейса
                        на чартерный;</li>
                    <li>поли-чартер – перелет, в котором состыковываются несколько чартерных
                        рейсов.</li>
                </ul>
                <p>Условия чартера своеобразные, немного жесткие, с ними нужно ознакомиться на
                    стадии бронирования билета:</p>
                <ul>
                    <li>вылеты часто задерживаются, потому что авиаперевозчик обязан отдавать
                        преимущество регулярным рейсам, а после них отправлять чартеры;</li>
                    <li>не всегда есть выбор класса обслуживания, в полете не стоит ожидать
                        разнообразного питания, других услуг повышенного комфорта;</li>
                    <li>вылеты часто производятся в неудобное для пассажиров время –
                        ночью, ранним утром, билеты можно получить в день вылета, иногда прямо в
                        аэропорту перед посадкой;</li>
                    <li>обычно билеты не подлежат возврату, обмену, на них нет
                        скидок и льгот.</li>
                </ul>
                <p>Несмотря на жесткость условий, рассматривая регулярный или чартерный рейс,
                    пассажиры отдают предпочтение чартеру за дешевые билеты. Забронировать место на
