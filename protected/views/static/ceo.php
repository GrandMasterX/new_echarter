                <h3>Полезная информация пассажирам чартерного рейса</h3>
                <p>Пассажиры, купившие билеты на чартер, имеют такие же права на обслуживание в
                    аэропорту, как и обладатели билетов на регулярный рейс:</p>
                <ul>
                    <li>если вылет задержан по вине авиакомпании более чем на 2 часа, пассажир
