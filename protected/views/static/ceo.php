                        семьям с детьми до 7 лет – обслуживание в комнате матери и ребенка.</li>
                </ul>
                <p>Если ваш рейс задерживается с вылетом, отметьте задержку в своем
                    авиабилете, сохраняйте чеки за услуги кафе или гостиницы – эти расходы
                    авиакомпания потом компенсирует на основании вашего заявления и прикрепленных к
                    нему расходных документов.</p>
